package zadania;

public class WyswietlanieZadania {

    public static void main(String[] args) {


        //1. Wyświetlanie imiienia

        System.out.println("Imię");

        //2. Wyświetlanie dwóch łańcuchów znaków

        System.out.print("af3r55");
        System.out.print("awfawfr2");

        //4. Wyrównanie do 15 znaków

        System.out.printf("%15s", "SDAASDASD    ");

        //5. Wyświetlanie hexadecymalne
        System.out.printf("%02x", 123);

    }
}
