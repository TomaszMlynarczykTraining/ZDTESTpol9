package dodatkowe_zadania.java_podstawy_dzien_drugi;

public class WhilePrzykłady {

    public static void main(String[] args) {
//while
        int iterator = 0;
        while (iterator < 5) {
            System.out.println("Nieskończoność");
            iterator++;
        }

        //do while

        int i = 5;
        do {
            System.out.println("Pierwsze wyświetlenie");
            i++;
        } while (i < 8);
    }


}
